function addNums(a: number, b: number) {
  console.log(`The sum of ${a} and ${b} is: ${a + b}`);
}
function substractNums(a: number, b: number) {
  console.log(`The substraction of ${a} and ${b} is: ${a - b}`);
}
function multiplyNums(a: number, b: number) {
  console.log(`The multiplication of ${a} and ${b} is: ${a * b}`);
}
